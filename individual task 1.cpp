#include <iostream> // Подключаем библиотеку для ввода-вывода
#include <cmath> // Подключаем математическую библиотеку для sin, sqrt

using namespace std; // Используем стандартное пространство имен

int main() // Главная функция программы
{
    double x; // Объявляем переменную x для хранения входного значения
    
    // Запрашиваем у пользователя ввод значения x
    cout << "Введите значение x: ";
    cin >> x; // Считываем значение x с клавиатуры
    
    // Проверяем, что выражение под первым корнем (x+1) неотрицательно
    if (x + 1 < 0) 
    {
        // Выводим сообщение об ошибке если условие не выполняется
        cout << "Ошибка: выражение под первым корнем отрицательное!" << endl;
        return 1;    // Завершаем программу с кодом ошибки 1
    }
    
    // Проверяем, что выражение под вторым корнем (x-1) неотрицательно
    if (x - 1 < 0) 
    {
        // Выводим сообщение об ошибке если условие не выполняется
        cout << "Ошибка: выражение под вторым корнем отрицательное!" << endl;
        return 1;    // Завершаем программу с кодом ошибки 1
    }

    double result = sin(sqrt(x + 1)) - sin(sqrt(x - 1));
    
    // Устанавливаем точность вывода (4 знака после запятой)
    cout.precision(4);
    // Выводим результат вычислений
    cout << "Результат выражения sin√(x+1) - sin√(x-1) = " << fixed << result << endl;
    
    return 0; // Успешное завершение программы
}